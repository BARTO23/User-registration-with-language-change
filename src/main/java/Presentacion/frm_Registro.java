    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        if (txtIdentificacionR.getText().equals("")){
           if(cbxIdioma.getSelectedIndex()==1){
            JOptionPane.showMessageDialog(null, "Debe ingresar dato a buscar",
            "BUSCAR", JOptionPane.ERROR_MESSAGE);
           }
           if(cbxIdioma.getSelectedIndex()==2){
               JOptionPane.showMessageDialog(null, "You must enter data to search");
           }

           txtIdentificacionR.setText("");
             txtIdentificacionR.requestFocus();
       }else{
        for (int i = 0; i <afectados.size(); i++){
            if (txtIdentificacionR.getText().equals(afectados.get(i).getId())){
               sw= 1;
               encuentra = i;
            }
        }
        if (sw == 0){
            btnActualizar.setEnabled(false);
            if(cbxIdioma.getSelectedIndex() == 1){
            JOptionPane.showMessageDialog(null, "La información no se ha registrado o encontrado",
                    "ERROR", JOptionPane.ERROR_MESSAGE);
            }
            if(cbxIdioma.getSelectedIndex()==2){
                JOptionPane.showMessageDialog(null, "The information has not been recorded");
            }
        }else{
            if(cbxIdioma.getSelectedIndex() == 1){
            JOptionPane.showMessageDialog(null, "Se ha encontrado la información con éxito", "BUSCAR",
                  JOptionPane.INFORMATION_MESSAGE);
            }
            if(cbxIdioma.getSelectedIndex() == 2){
                JOptionPane.showMessageDialog(null, "The information was successfully found");
            }

            btnActualizar.setEnabled(true);
            txtApellidoR.setText("" + afectados.get(encuentra).getStrApellido());
            txtBarrioR.setText("" + afectados.get(encuentra).getStrBarrio());
            txtCantHabitR.setText("" + afectados.get(encuentra).getIntCantHabitantes());
            txtContactoR.setText("" + afectados.get(encuentra).getStrContacto());
            txtCorreoR.setText("" + afectados.get(encuentra).getStrcorreo());
            txtEdadR.setText(""+ afectados.get(encuentra).getIntEdad());
            txtIdentificacionR.setText("" + afectados.get(encuentra).getId());
            txtAfectacionR.setText("" + afectados.get(encuentra).getStrAfectacion());
            txtNombreR.setText("" + afectados.get(encuentra).getStrName());
            cbxSexoR.setSelectedItem("" + afectados.get(encuentra).getStrSexo());
            
        }
       }
    }//GEN-LAST:event_btnBuscarActionPerformed

