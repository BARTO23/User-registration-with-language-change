private void fnt_Guardar( String codigo){
    int contador = 0;
        for(int i = 0; i < fila; i++){
            if(codigo.equals(Ayudas[i][1])){
                contador++;
            }
        }
        if(contador == 3){
            if(cbxIdioma.getSelectedIndex()==0){
                lblMensajeAyudas.setText("Has agotado todas tus ayudas");
            }
            if(cbxIdioma.getSelectedIndex()==1){
                lblMensajeAyudas.setText("You have exhausted all your aid");
            }
        }else{
            Ayudas[fila][0] = txtItemA.getText();
            Ayudas[fila][1] = codigo;
            Ayudas[fila][2] = lblColocarFecha.getText();
            Ayudas[fila][3] = cbxTipoAyudaA.getSelectedItem().toString();
            fila++;
            if(cbxIdioma.getSelectedIndex()==1){
                JOptionPane.showMessageDialog(null, "Registro éxitoso");
            }
            if(cbxIdioma.getSelectedIndex()==2){
                JOptionPane.showMessageDialog(null, "nice");
            }
        }
    }
